#Faça um Programa que pergunte quanto você ganha por hora e o número de horas trabalhadas no mês. 
# Calcule e mostre o total do seu salário no referido mês.
salario_hora = float(input('Digite o valor recebido por hora: '))
horas_trabalhada = float(input('Digite o total de hora trabalhada  no mês: '))

Salario_mes = salario_hora * horas_trabalhada

print('O salario do mês: ', Salario_mes)